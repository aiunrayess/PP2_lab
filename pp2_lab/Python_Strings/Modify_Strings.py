#Uppercase
a = "Hello, World!"
print(a.upper())

#Lowercase
a = "Hello, World!"
print(a.lower())

#Removing Whitespace
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!" 

#Replace string
a = "Hello, World!"
print(a.replace("H", "J"))

#Split String
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!'] 